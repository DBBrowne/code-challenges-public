function badApples(input) {
  // console.log(input)
  var halfPackageIndex = -1
  const output = []

  input.forEach((pair) => {
    // console.log(pair)
    // check each apple
    var goodApples = pair.filter(apple => {
      return apple > 0
    })
    // if package is all bad, do not keep
    if (0 === goodApples.length) {
      return
    }
    // if package is all good, keep entire package
    if (2 === goodApples.length){
      output.push(pair)
      return
    }
    // if one apple is good and half package exists, add apple and reset halfPackageIndex
    if (halfPackageIndex > -1){
      output[halfPackageIndex].push(goodApples[0])
      halfPackageIndex = -1
      return
    }
    // otherwise keep the apple, but record the package index for disposal if pair cannot be found
    halfPackageIndex = output.length
    output.push(goodApples)
  })

  return output.filter((pair,i) => {
    return !!pair && i !== halfPackageIndex
  })
}

