function codeWarsSolution(input) {
  const singles = input
    .reduce( (sgs,[a,b]) => sgs.concat( a == 0 && b > 0 ? [b] : b == 0 && a > 0 ? [a] : []), [] )
    .reduce( (sgs,a,i,aa) => i % 2 == 0 ? sgs.concat( [aa.slice(i,i + 2)], 0) : sgs, [] )
  return input.map( ([a,b]) => a < 1 || b < 1 ? singles.shift() : [a,b] ).filter( ab => ab && ab.length > 1 )
}

